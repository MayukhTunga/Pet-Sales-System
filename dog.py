from pet import *

class Dog(Pet):
    def getAttributes(self, name, gender, dob, age, height, weight, breed, trained, affectionate):
        self.name = name
        self.gender = gender
        self.dob = dob
        self.age = age
        self.height = height
        self.weight = weight
        self.breed = breed
        self.trained = trained
        self.affectionate = affectionate
    



